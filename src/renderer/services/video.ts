export function getPlaylists() {
  return youtubePlaylistsResponse.items.map<IdentiflixPlaylist>(playlist => ({
    id: playlist.id,
    title: playlist.snippet.title,
    videos: youtubeVideosResponse.items.filter(video => video.snippet.playlistId === playlist.id).map(video => ({
      id: video.contentDetails.videoId,
      title: video.snippet.title,
      description: video.snippet.description,
      thumbnail: video.snippet.thumbnails.maxres.url,
    }))
  }));
}

export function getVideo(videoId: string) {
  const videos =  youtubeVideosResponse.items.filter(video => video.contentDetails.videoId === videoId).map<IdentiflixVideo>(video => ({
    id: video.contentDetails.videoId,
    title: video.snippet.title,
    description: video.snippet.description,
    thumbnail: video.snippet.thumbnails.maxres.url,
  }));

  if (videos.length) {
    return videos[0];
  }
}
